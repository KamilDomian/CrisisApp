package app.crisis.CrisisApp;

public class FinancialExpenses {
    private int credits;
    private int FinanceCharges;
    private int SpendingPleasures;
    private int PetExpenses;


    public FinancialExpenses(int credits, int financeCharges, int spendingPleasures, int petExpenses) {
        this.credits = credits;
        this.FinanceCharges = financeCharges;
        this.SpendingPleasures = spendingPleasures;
        this.PetExpenses = petExpenses;

    }
}
